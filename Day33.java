package isc;

public class day33 {
    
    public static void main(String[] args) {
        int abc = 48;  // Tipe data int
        double zee = abc;  // Otomatis casting dari int ke double

        System.out.println("Nilai int: " + abc);  // Output: 48
        System.out.println("Nilai double (setelah casting): " + zee);  // Output: 48.0
    }
    
}
