package isc;

public class day25 {
    
    public static void main(String[] args) {
        System.out.println("OPERATOR ARITMATIKA");
        Double no=3.0;
        System.out.println("angka pertama: "+no);
        Double ok=4.0;
        System.out.println("angka kedua: "+ok);
        System.out.println("|•H•A•S•I•L•|");
        System.out.printf("hasil tambah: %f \n",(no+ok));
        System.out.printf("hasil kurang: %f \n",(no-ok));
        System.out.printf("hasil kali: %f \n", (no*ok));
        System.out.printf("hasil bagi %f \n", (no/ok));
        System.out.printf("hasil sisa bagi: %f \n",(no%ok));
    }
    
}
