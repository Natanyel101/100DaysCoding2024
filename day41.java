import java.util.*;
public class imputan {
    
    public static void main(String[] args) {
        Scanner zee=new Scanner(System.in);
        int a=zee.nextInt();
        int b=zee.nextInt();
        System.out.println("hasil tambah: "+(a+b));
        System.out.println("hasil kutang: "+(a-b));
        System.out.println("hasil kali: "+(a*b));
        System.out.println("hasil bagi: "+(a/b));
        System.out.println("hasil sisah bagi: "+(a%b));
    }
    
}
